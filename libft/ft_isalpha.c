int	ft_isalpha(int c)
{
	int yes_no;

	yes_no = 0;
	if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A'))
	{
		yes_no = 1;
	}
	return (yes_no);
}
