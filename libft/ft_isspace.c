#include "libft.h"

int	ft_isspace(int c)
{
	int yes_no;

	yes_no = 0;
	if (c == ' ' || c == '\n' || c == '\t')
	{
		yes_no = 1;
	}
	else if (c == '\r' || c == '\v' || c == '\f')
	{
		yes_no = 1;
	}
	return (yes_no);
}
