#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	k;

	if (size == 0)
		return (ft_strlen(src));
	if (dst == 0 || src == 0)
		return (0);
	k = 0;
	while (src[k])
		k++;
	i = 0;
	while (i < size - 1 && src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (k);
}
